package om;

public class student1   {

	  int rno;
	  int marks;
	  String name;
	public student1(int rno, int marks, String name) {
		super();
		this.rno = rno;
		this.marks = marks;
		this.name = name;
		
	}
	@Override
	public String toString() {
		return "\nstudent1 [rno=" + rno + ", marks=" + marks + ", name=" + name + "]";
	}
	
	
	
	  

}
